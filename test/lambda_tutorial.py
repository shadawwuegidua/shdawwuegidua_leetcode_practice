# Lambda 表达式（匿名函数）入门

# 1. 什么是 Lambda？
# Lambda 是一种快速定义单行函数的方法。
# 因为它没有名字，所以也叫"匿名函数"。

# 语法格式：
# lambda 参数: 表达式


# --- 对比一：普通函数 vs Lambda ---

# 普通函数写法
def add(x, y):
    return x + y

print("普通函数:", add(3, 5))

# Lambda 写法
# 这里的 add_lambda 只是一个变量名，指向那个匿名函数
add_lambda = lambda x, y: x + y

# [回答] 是的，理解完全正确！
# 1. 参数：lambda x, y 中的 x, y 就是函数的参数。
# 2. 返回值：冒号 : 后面的表达式 (x + y) 的计算结果，会自动作为返回值返回。
# 3. 类型：Python 是动态类型语言。x 和 y 可以是任何支持 + 操作的类型。
#    - 如果传整数，返回值就是整数。
#    - 如果传字符串，返回值就是拼接后的字符串。

print("Lambda  :", add_lambda(3, 5))


# --- 对比二：为什么要用它？ ---
# Lambda 最常用的场景是：作为一个参数，传给另一个函数（比如排序）。

# 只有三行数据的成绩单：[("姓名", 分数)]
scores = [("小明", 90), ("小红", 100), ("小刚", 80)]
# [回答] 是的，圆括号 () 包裹的就是"元组" (Tuple)。
# 元组和列表很像，区别在于：
# 1. 写法不同：列表用 []，元组用 ()。
# 2. 也是最重要的区别：列表可以修改（增加、删除元素），但元组一旦创建就**不能修改** (Immutable)。
#    它适合用来存那些固定搭配的数据，比如这里的 (姓名, 分数) 组合。

# 需求：按分数(第2个元素)从低到高排序
# sort 函数需要知道按什么规则排。

# 方法A：定义一个专门的函数告诉它
def get_score(item):
    return item[1]  # 返回分数部分
scores.sort(key=get_score) 
print("排序后(普通方法):", scores)

# [回答]
# 1. key 参数的作用：它是排序的"依据"。
#    默认情况下，sort 会直接比较元素本身。但这里的元素是元组 ("小明", 90)，
#    直接比较元组规则比较复杂。如果你只想根据"分数"来排，就需要用 key。
#    用法：key 接收一个函数。Python 会把列表里的每个元素都扔给这个函数，
#    然后根据这个函数返回的值（比如 90），来进行排序。
#
# 2. scores.sort()：
#    是的，`scores` 是列表(list)这个"类"的一个实例对象。
# 这里使用了 Lambda 配合 key 参数：
# key=lambda x: x[1]
# 意思是：对于列表中的每个元素 x（也就是每个 ("姓名", 分数) 元组），
# 我只要它的第2项 x[1]（分数）作为排序的依据。
# 这和上面定义的 get_score 函数作用完全一样，只是写在一行里了。
scores.sort(key=lambda x: x[1])

# 重置数据
scores = [("小明", 90), ("小红", 100), ("小刚", 80)]

# 方法B：直接用 Lambda (更简洁，不用专门去写一个 def)
# 这里的 `x` 代表列表里的每一个元素 ("姓名", 分数)
# `x[1]` 代表我们要用分数来做比较
scores.sort(key=lambda x: x[1])
print("排序后(Lambda):  ", scores)


# --- 总结 ---
# 1. 它就是个只有一行的函数。
# 2. 只有在函数非常简单，且只用一次的时候用它最方便。
# 3. 冒号左边是参数，右边是返回值。
